    public function run()
    {
        Model::unguard();
        
        $evaders = $this->evaderList();

        foreach ($evaders as $evader) {
            $banEvader = new BanEvader();
            $banEvader->save();

            if (is_array($evader["profiles"]) && count($evader["profiles"]) > 0) {
                $this->addAccounts($banEvader, $evader["profiles"]);
            }
            if (is_array($evader["ips"]) && count($evader["ips"]) > 0) {
                $this->addIps($banEvader, $evader["ips"]);
            }

        }
        
        Model::reguard();
    }

    public function makeAccount()
    {
        return new BanEvader();
    }

    public function addAccounts(BanEvader $evader, $accounts)
    {
        foreach ($accounts as $account) {
            $profile = new BanEvaderProfile();
            $profile->profile_id = $account;
            $profile->ban_evader_id = $evader->id;
            $profile->save();
        }
    }

    public function addIPs(BanEvader $evader, $ips)
    {
        foreach ($ips as $ip) {
            $range = new BanEvaderIPRange();
            $rangeIPs = BanEvaderIPRange::fromTagProRange($ip);
            $range->first_address = $rangeIPs['first_address'];
            $range->last_address = $rangeIPs['last_address'];
            $range->ban_evader_id = $evader->id;
            $range->save();
        }
    }

